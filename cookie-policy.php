Cookies Policy.

0. COSA SONO I COOKIE
Il sito www.sitocliente.it utilizza i cookie per rendere i propri servizi semplici ed efficienti per
l’utenza che ne visiona le pagine. I cookie sono piccoli file di testo che i siti visitati dagli utenti
inviano ai loro terminali, dove vengono memorizzati per essere ritrasmessi agli stessi siti in
occasione di visite successive. I cookie sono utilizzati per diverse finalità, hanno caratteristiche
diverse, e possono essere utilizzati sia dal titolare del sito che si sta visitando, sia da terze parti.
Esistono vari tipi di cookie, a seconda delle loro caratteristiche e funzioni.

1. COOKIE TECNICI
Questa tipologia di cookie permette il corretto funzionamento di alcune sezioni del sito. I cookie
tecnici sono di due categorie:
• persistenti: una volta chiuso il browser non vengono distrutti ma rimangono fino ad una data di
scadenza preimpostata;
• di sessione: vengono distrutti ogni volta che il browser viene chiuso.

Questi cookie sono necessari per visualizzare correttamente il sito e in relazione ai servizi tecnici
offerti, verranno quindi sempre utilizzati e inviati, a meno che l’utenza non modifichi le
impostazioni nel proprio browser. Tali cookie non richiedono un consenso esplicito per il loro
utilizzo. Sono considerati cookie tecnici anche:
• i “cookie analytics” nel caso vengano utilizzati direttamente dal gestore del sito per raccogliere
informazioni in forma aggregata, sul numero di utenti e su come questi visitano il sito;
• i cookie di navigazione o di sessione (necessari per l’autenticazione);
• i cookie di funzionalità che migliorano l’esperienza di navigazione dell’utente.

2. COOKIE DI PROFILAZIONE
Questa tipologia di cookie è volta a creare profili relativi all'utente e utilizzati al fine di inviare
messaggi pubblicitari in linea con le preferenze manifestate dallo stesso nell'ambito della
navigazione in rete. In questo caso, è richiesto un preventivo consenso dell'utente.

