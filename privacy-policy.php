2. TIPOLOGIE DI DATI RACCOLTI
Il Titolare del Trattamento, informa che, per il normale funzionamento del sito, tratterà dati che per
loro natura possono essere definiti come dati personali. Per dato personale si intende qualunque
informazione relativa a persona fisica, identificata o identificabile, anche indirettamente, mediante
riferimento a qualsiasi altra informazione, ivi compreso un numero di identificazione personale.

I dati personali trattati attraverso il sito sono i seguenti:
a) Dati di Navigazione
Durante il normale funzionamento del sito, i sistemi informatici e le procedure software
acquisiscono alcuni dati personali la cui trasmissione è implicita nell'uso dei protocolli di
comunicazione di Internet. Si tratta di informazioni che non sono raccolte per essere associate a
interessati identificati, ma che per loro stessa natura potrebbero, attraverso elaborazioni ed
associazioni con dati detenuti da terzi, permettere di identificare gli utenti. In questa categoria di
dati rientrano gli indirizzi IP o i nomi a dominio dei computer utilizzati dagli utenti che si
connettono al sito, gli indirizzi in notazione URI (Uniform Resource Identifier) delle risorse
richieste, l'orario della richiesta, il metodo utilizzato nel sottoporre la richiesta al server, la
dimensione del file ottenuto in risposta, il codice numerico indicante lo stato della risposta data dal
server (buon fine, errore, ecc.) ed altri parametri relativi al sistema operativo e all'ambiente
informatico dell'utente.

b) Dati personali comuni
Per accedere a determinati servizi del sito, potrebbe essere richiesto il conferimento di dati personali
comuni. Tali dati personali potrebbero essere, a titolo esemplificativo e non esaustivo: nome,
cognome, indirizzo e-mail, numero di telefono, ecc.

c) Speciali categorie di dati personali
Nell’utilizzo del form della sezione contatti per l’invio di candidature, potrebbe verificarsi un
conferimento di dati personali rientranti nelle categorie citate dall’art. 9 del Regolamento, i
cosiddetti dati particolari: “[…] dati idonei a rivelare l'origine razziale o etnica, le opinioni
politiche, le convinzioni religiose o filosofiche, o l'appartenenza sindacale, nonché […] dati
genetici, dati biometrici intesi a identificare in modo univoco una persona fisica, dati relativi alla
salute o alla vita sessuale o all'orientamento sessuale della persona”. Tali categorie di dati verranno
trattati previo consenso da parte dell’interessato.
